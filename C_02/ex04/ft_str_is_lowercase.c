int		ft_str_is_lowercase(char *str)
{
	int check;
	int i;

	check = 1;
	i = 0;
	while (str[i] != '\0' && check == 1)
	{
		if (!(str[i] > 96 && str[i] < 123))
			check = 0;
		i++;
	}
	return (check);
}
