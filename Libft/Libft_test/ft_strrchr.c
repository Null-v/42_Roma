char	*ft_strrchr(char *s, int c)
{
	char *p;
	char ch;

	p = 0;
	ch = c;
	while (*s != '\0')
	{
		if (*s == ch)
			p = s;
		s++;
	}
	if (*s == '\0' && c == 0)
		p = s;
	return (p);
}
